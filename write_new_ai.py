# ── PHASE 1: Аналіз структури документа ────────────────────────────────────
L("/**")
L(" * Розбирає текст на рядки та класифікує кожен:")
L(" *   'def'    — рядок з явним визначенням (термін - визначення)")
L(" *   'header' — заголовок розділу")
L(" *   'prose'  — звичайний текст без явного визначення")
L(" * Повертає { defLines, proseBlocks, glossaryRatio }")
L(" */")
L("function detectDocumentStructure(text) {")
L("  const lines = text.split('\\n');")
L("  const defLines   = [];  // рядки з явними визначеннями")
L("  const proseLines = [];  // решта тексту")
L()
L("  // Паттерн для рядка-визначення: щось - щось АБО щось — щось")
L("  const DEF_LINE = /^([^\\n]{3,80}?)\\s*(?:-|\\u2014|\\u2013)\\s+([^\\n]{15,})$/;")
L("  // Паттерн заголовку: коротко, часто великими або з номером")
L("  const HEADER = /^(\\d[\\d.]*\\.?\\s+)?[А-ЯІЇЄA-Z][^\\n]{0,80}$/;")
L()
L("  for (const line of lines) {")
L("    const trimmed = line.trim();")
L("    if (!trimmed || trimmed.length < 3) continue;")
L()
L("    if (DEF_LINE.test(trimmed)) {")
L("      defLines.push(trimmed);")
L("    } else if (trimmed.length < 100 && HEADER.test(trimmed)) {")
L("      // заголовки пропускаємо (не prose і не def)")
L("    } else {")
L("      proseLines.push(trimmed);")
L("    }")
L("  }")
L()
L("  const total = defLines.length + proseLines.length;")
L("  const glossaryRatio = total > 0 ? defLines.length / total : 0;")
L("  log(`[Structure] def=${defLines.length} prose=${proseLines.length} ratio=${glossaryRatio.toFixed(2)}`);")
L()
L("  return {")
L("    defLines,")
L("    proseText: proseLines.join('\\n\\n'),")
L("    glossaryRatio,")
L("  };")
L("}")
L()

# ── PHASE 2: Швидке витягування з рядків-визначень ─────────────────────────
L("/**")
L(" * Швидкий heuristic-екстрактор для явно визначених рядків.")
L(" * Не потребує LLM. Повертає масив термінів з категоріями.")
L(" */")
L("function extractFromDefinitionLines(defLines) {")
L("  const found = new Map();")
L()
L("  for (const line of defLines) {")
L("    let term = null, def = null;")
L()
L("    // Тип A: em/en dash  \"Термін — визначення\"")
L("    const mDash = line.match(/^([^\\u2014\\u2013\\n]{3,80}?)\\s*[\\u2014\\u2013]\\s*(.{15,})$/);")
L("    if (mDash) { term = mDash[1]; def = mDash[2]; }")
L()
L("    // Тип B: одинарний дефіс  \"термін - визначення;\"  (формат ЗСУ)")
L("    if (!term) {")
L("      const mHyp = line.match(/^([Ѐ-ӿA-Za-z][^\\n]{2,79}?)\\s+-\\s+([Ѐ-ӿ\\w].{14,}?)[;.]?$/);")
L("      if (mHyp && (mHyp[1].match(/-/g)||[]).length < 3)")
L("        { term = mHyp[1]; def = mHyp[2]; }")
L("    }")
L()
L("    // Тип C: \"термін це/є/означає визначення\"")
L("    if (!term) {")
L("      const mIs = line.match(/^([Ѐ-ӿA-Za-z][^\\n]{2,60}?)\\s+(?:це|є|означає)\\s+(.{15,})$/i);")
L("      if (mIs) { term = mIs[1]; def = mIs[2]; }")
L("    }")
L()
L("    if (!term || !def) continue;")
L()
L("    term = term.trim().replace(/^\\d+[\\.)\\s]+/, '').trim();")
L("    def  = cleanDefinition(def);")
L()
L("    if (term.length < 3 || term.length > 80 || def.length < 10 || /^\\d+$/.test(term)) continue;")
L()
L("    const key = term.toLowerCase();")
L("    if (!found.has(key)) {")
L("      found.set(key, {")
L("        term,")
L("        definition: def,")
L("        category: classifyTermByKeywords(term, def),")
L("        extended_info: '',")
L("        definition_source_type: 'Document',")
L("      });")
L("    }")
L("  }")
L()
L("  return Array.from(found.values());")
L("}")
L()

# ── PHASE 3: LLM тільки для prose-тексту ───────────────────────────────────
L("/**")
