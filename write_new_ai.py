L("  try {")
L("    const res = await fetch(`${OLLAMA_URL}/api/generate`, {")
L("      method: 'POST', headers: { 'Content-Type': 'application/json' },")
L("      body: JSON.stringify({ model: OLLAMA_MODEL, prompt, format: 'json', stream: false,")
