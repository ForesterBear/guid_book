L("  if (!text.trim()) throw new Error('Документ порожній або не вдалось розпізнати текст.');")
L("  text = cleanText(text);")
L("  log(`[Pipeline] Текст очищено: ${text.length} символів`);")
L()
L("  // ── Phase 1: Аналіз структури ────────────────────────────────────────")
L("  await progressCallback(10, 'Аналіз структури документа...');")
L("  const { defLines, proseText, glossaryRatio } = detectDocumentStructure(text);")
L()
L("  const uniqueMap = new Map();")
L()
L("  // ── Phase 2: Миттєве витягування з def-рядків ────────────────────────")
L("  await progressCallback(20, `Знайдено ${defLines.length} рядків визначень. Обробка...`);")
L("  const heuristicTerms = extractFromDefinitionLines(defLines);")
L("  log(`[Heuristic] Витягнуто ${heuristicTerms.length} термінів`);")
