# ── chunkText ───────────────────────────────────────────────────────────────
L("function chunkText(text, maxLength, overlapChars = 200) {")
L("  const paragraphs = text.replace(/\\r\\n/g, '\\n').split(/\\n{2,}/).map(p=>p.trim()).filter(Boolean);")
L("  const chunks = []; let current = '', prevTail = '';")
L("  for (const para of paragraphs) {")
L("    if (para.length > maxLength) {")
L("      const sents = para.split(/(?<=[.!?;])\\s+/);")
L("      for (const s of sents) {")
L("        if ((current+' '+s).trim().length > maxLength) {")
L("          if (current) { chunks.push(current.trim()); prevTail = current.slice(-overlapChars); current = prevTail+'\\n\\n'+s; }")
L("          else { chunks.push(s.trim()); prevTail = s.slice(-overlapChars); current = prevTail; }")
L("        } else current = current ? current+' '+s : s;")
L("      }")
L("      continue;")
L("    }")
L("    const cand = current ? current+'\\n\\n'+para : para;")
L("    if (cand.length > maxLength) { chunks.push(current.trim()); prevTail = current.slice(-overlapChars); current = prevTail+'\\n\\n'+para; }")
L("    else current = cand;")
L("  }")
L("  if (current.trim()) chunks.push(current.trim());")
L("  return chunks;")
L("}")
