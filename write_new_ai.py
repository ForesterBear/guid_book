L("    else if (ext === 'xlsx' || ext === 'xls') text = await extractTextFromXLSX(filePath);")
L("    else if (ext === 'txt')  text = fs.readFileSync(filePath, 'utf8');")
L("    else throw new Error('Unsupported file type: ' + ext);")
