L()
L("  if (heuristicTerms.length > 0) {")
L("    await progressCallback(40,")
L("      `Heuristic: знайдено ${heuristicTerms.length} термінів`,")
L("      heuristicTerms);")
L("  }")
L()
L("  // ── Phase 3: LLM для prose (тільки якщо потрібно) ────────────────────")
L("  const GLOSSARY_THRESHOLD  = 0.4;  // якщо > 40% рядків — явний словник")
L("  const MIN_HEURISTIC_TERMS = 3;    // якщо менше — документ без глосарію")
L()
L("  const needsLLM = proseText.trim().length > 200 &&")
L("    (glossaryRatio < GLOSSARY_THRESHOLD || heuristicTerms.length < MIN_HEURISTIC_TERMS);")
L()
L("  if (needsLLM) {")
L("    log(`[Pipeline] Запуск LLM для prose (ratio=${glossaryRatio.toFixed(2)})`);")
