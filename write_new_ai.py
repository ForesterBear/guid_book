L("  if (!proseText || proseText.trim().length < 100) return [];")
L()
L("  const retry = isRetry ? 'IMPORTANT: Return ONLY a valid JSON array, no other text.\\n\\n' : '';")
L("  const prompt = retry + `You are a Ukrainian military terminology extractor.")
