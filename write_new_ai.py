# ── Очищення тексту ─────────────────────────────────────────────────────────
L("function cleanDefinition(def) {")
L("  return def.replace(/[;]\\s*$/, '').replace(/^[\\u00AB\\u00BB\\u201C\\u201E\"']|[\\u00AB\\u00BB\\u201D\"']$/g, '').trim();")
