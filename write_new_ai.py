L("    if (result.length === 0 && !isRetry) return extractFromProse(proseText, knownKeys, true);")
L("    return result;")
L("  } catch (e) {")
L("    log('[AI prose] error:', e.message);")
L("    return [];")
L("  }")
L("}")
L()

